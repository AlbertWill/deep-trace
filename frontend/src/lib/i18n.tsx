const STORAGE_KEY = "vibe-trading-locale";

function detectLocale(): Locale {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored === "en" || stored === "zh") return stored;
  if ((navigator.language || "").startsWith("zh")) return "zh";
  return "en";
}

